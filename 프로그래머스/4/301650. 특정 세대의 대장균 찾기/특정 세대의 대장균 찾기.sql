WITH A AS(  #1 ID
SELECT ID
FROM ECOLI_DATA 
WHERE PARENT_ID IS NULL
),
B AS(   #2 ID
SELECT E.ID 
FROM A 
LEFT JOIN ECOLI_DATA E
ON A.ID = E.PARENT_ID
)
SELECT E.ID
FROM B 
LEFT JOIN ECOLI_DATA E
ON B.ID = E.PARENT_ID
WHERE E.ID IS NOT NULL
ORDER BY E.ID






