WITH A AS(
SELECT O.NAME AS NAME, DATEDIFF(O.DATETIME,I.DATETIME) AS DDATE, I.ANIMAL_ID AS ID
FROM ANIMAL_INS AS I
JOIN ANIMAL_OUTS AS O
ON I.ANIMAL_ID = O.ANIMAL_ID
ORDER BY DDATE DESC
)
SELECT ID AS ANIMAL_ID,NAME
FROM A LIMIT 2





